int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n, tt;
  cin >> n >> tt;
  vector<long long> a(n);
  vector<int> w(n);
  vector<int> v(n);
  for (int i = 0; i < n; i++) {
    cin >> a[i] >> w[i] >> v[i];
  }
  vector<int> order(n);
  iota(order.begin(), order.end(), 0);
  sort(order.begin(), order.end(), [&](int i, int j) {
    if (v[i] != v[j]) {
      return v[i] > v[j];
    }
    if (w[i] != w[j]) {
      return w[i] < w[j];
    }
    return i < j;
  });
  vector<int> pos_order(n);
  for (int i = 0; i < n; i++) {
    pos_order[order[i]] = i;
  }
  const int BITS = 20;
  vector<segtree> st(BITS, segtree(n));
  vector<omg::segtree> ts(BITS, omg::segtree(n));
  for (int i = 0; i < n; i++) {
    if (a[i] > 0) {
      int bit = 31 - __builtin_clz(w[i]);
      ts[bit].modify(pos_order[i], pos_order[i], -inf);
      for (int b = bit + 1; b < BITS; b++) {
        st[b].modify(pos_order[i], pos_order[i], (long long) a[i] * w[i], (long long) a[i] * v[i]);
        ts[b].modify(pos_order[i], n - 1, (long long) a[i] * w[i]);
      }
    }
  }
  while (tt--) {
    int type;
    cin >> type;
    if (type == 1) {
      int k, d;
      cin >> k >> d;
      --d;
      int bit = 31 - __builtin_clz(w[d]);
      for (int b = bit + 1; b < BITS; b++) {
        st[b].modify(pos_order[d], pos_order[d], (long long) k * w[d], (long long) k * v[d]);
        ts[b].modify(pos_order[d], n - 1, (long long) k * w[d]);
      }
      if (a[d] == 0) {
        ts[bit].modify(pos_order[d], pos_order[d], -inf);
      }
      a[d] += k;
    }
    if (type == 2) {
      int k, d;
      cin >> k >> d;
      --d;
      int bit = 31 - __builtin_clz(w[d]);
      for (int b = bit + 1; b < BITS; b++) {
        st[b].modify(pos_order[d], pos_order[d], (long long) -k * w[d], (long long) -k * v[d]);
        ts[b].modify(pos_order[d], n - 1, (long long) -k * w[d]);
      }
      a[d] -= k;
      if (a[d] == 0) {
        ts[bit].modify(pos_order[d], pos_order[d], +inf);
      }
    }
    if (type == 3) {
      long long c;
      cin >> c;
      long long ans = 0;
      int pos = 0;
      while (pos < n && c > 0) {
        debug(pos, c);
        int bit = 63 - __builtin_clzll(c);
        if (bit < BITS) {
          // find smth
          long long sub = 0;
          if (pos > 0) {
            sub = st[bit].get(0, pos - 1).sw;
          }
          int who = ts[bit].find_first(pos, n - 1, [&](const omg::segtree::node& nd) {
            return nd.mn - sub <= c;
          });
          if (who != -1) {
            if (pos < who) {
              auto got = st[bit].get(pos, who - 1);
              c -= got.sw;
              ans += got.sv;
            }
            assert(a[order[who]] > 0);
            long long take = min(a[order[who]], c / w[order[who]]);
            c -= take * w[order[who]];
            ans += take * v[order[who]];
            pos = who + 1;
            continue;
          }
        }
        bit = min(bit, BITS - 1);
        pos = st[bit].find_first(pos, n - 1, [&](const segtree::node& nd) {
//          debug(nd.sw, nd.sv);
          if (nd.sw <= c) {
            c -= nd.sw;
            ans += nd.sv;
            return false;
          }
          return true;
        });
        if (pos == -1) {
          break;
        }
        long long take = min(a[order[pos]], c / w[order[pos]]);
        c -= take * w[order[pos]];
        ans += take * v[order[pos]];
        pos += 1;
      }
      cout << ans << '\n';
    }
  }
  return 0;
}
